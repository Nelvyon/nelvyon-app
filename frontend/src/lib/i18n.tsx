function detectBrowserLocale(): Locale {
  const stored = localStorage.getItem("nelvyon-locale");
  if (stored && stored in localeNames) return stored as Locale;

  const browserLang = navigator.language || (navigator as { userLanguage?: string }).userLanguage || "es";
  const langCode = browserLang.split("-")[0].toLowerCase();

  const localeMap: Record<string, Locale> = {
    es: "es", en: "en", pt: "pt", fr: "fr", de: "de", it: "it", zh: "zh", ja: "ja", ko: "ko", ar: "ar",
  };

  return localeMap[langCode] || "es";
}

interface I18nContextType {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKeys) => string;
  td: (key: DashboardTranslationKeys) => string;
  ts: (key: SaasTranslationKeys) => string;
  /** Universal translator: works with any namespace/key via the registry */
  tx: (namespace: string, key: string) => string;
  /** Get all registered locales (for language selector) */
  availableLocales: () => { code: string; name: string; flag: string }[];
}

const I18nContext = createContext<I18nContextType>({
  locale: "es",
  setLocale: () => {},
  t: (key) => key,
  td: (key) => key,
  ts: (key) => key,
  tx: (_ns, key) => key,
  availableLocales: () => [],
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("es");

  useEffect(() => {
    const detected = detectBrowserLocale();
    setLocaleState(detected);
    // Set document direction for RTL languages
    document.documentElement.dir = getLocaleDir(detected);
  }, []);

  const setLocale = (newLocale: Locale) => {
    setLocaleState(newLocale);
    localStorage.setItem("nelvyon-locale", newLocale);
    document.documentElement.dir = getLocaleDir(newLocale);
  };

  const t = (key: TranslationKeys): string => {
    return translations[locale]?.[key] || translations.es[key] || key;
  };

  const td = (key: DashboardTranslationKeys): string => {
    return dashboardTranslations[locale]?.[key] || dashboardTranslations.es[key] || key;
  };

  const ts = (key: SaasTranslationKeys): string => {
    return saasTranslations[locale]?.[key] || saasTranslations.es[key] || key;
  };

  const tx = (namespace: string, key: string): string => {
    return getTranslation(locale, namespace, key);
  };

  const availableLocales = () => {
    return getRegisteredLocales().map((l) => ({
      code: l.code,
      name: l.name,
      flag: l.flag,
    }));
  };

  return (
    <I18nContext.Provider value={{ locale, setLocale, t, td, ts, tx, availableLocales }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}

