const esFull = deepMerge(es, shellNav.es);
const enBase = JSON.parse(fs.readFileSync(path.join(messagesDir, "en.json"), "utf8"));
const enFull = deepMerge(enBase, shellNav.en);

fs.writeFileSync(path.join(messagesDir, "es.json"), JSON.stringify(esFull, null, 2) + "\n");
fs.writeFileSync(path.join(messagesDir, "en.json"), JSON.stringify(enFull, null, 2) + "\n");

for (const locale of ["fr", "pt", "de", "it"]) {
  const merged = deepMerge(deepMerge(esFull, T[locale]), shellNav[locale]);
  fs.writeFileSync(path.join(messagesDir, `${locale}.json`), JSON.stringify(merged, null, 2) + "\n");
}

