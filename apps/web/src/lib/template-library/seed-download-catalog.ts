export function matchSeedForLayer(
  layer: "saas" | "os",
  sectorId: string,
  serviceId: string,
  kind: string,
  varietyKey?: string,
): SeedCatalogEntry {
  const keys =
    layer === "saas"
      ? SAAS_SERVICE_LEGACY_KEYS[serviceId] ?? ["Landing"]
      : OS_CAPABILITY_LEGACY_KEYS[serviceId] ?? ["Landing"];
  for (const key of keys) {
    const hit = matchSeed(sectorId, key, kind, varietyKey);
    if (hit) return hit;
  }
  return matchSeed(sectorId, "Landing", kind, varietyKey);
}

export function resolveRowLayer(
  layer: "saas" | "os",
  serviceId: string,
): "saas" | "os" | "both" {
  if (layer === "saas") {
    const s = getSaasService(serviceId);
    return s?.dual_os_id ? "both" : "saas";
  }
  const c = getOsCapability(serviceId);
  return c?.dual_saas_id ? "both" : "os";
}
