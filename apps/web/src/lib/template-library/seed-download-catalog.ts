export function matchSeed(
  sectorId: string,
  service: string,
  kind: string,
  varietyKey?: string,
): SeedCatalogEntry {
  const curatedMod = curatedPicker();
  const curated = curatedMod?.pickCuratedSeed({ sectorId, service, kind, varietyKey });
  if (curated && curatedMod) return curatedMod.curatedSeedToCatalogEntry(curated);

  const normalizedKind = normalizeSeedKind(kind);
  const group = SECTOR_GROUPS[sectorId] ?? "other";
  const candidates = SEED_DOWNLOAD_CATALOG.filter((s) => {
    if (!s.kinds.includes(normalizedKind) && !s.kinds.includes(kind)) return false;
    if (!s.services.includes(service) && !s.services.includes("Landing")) return false;
    if (s.match.sectors?.includes(sectorId)) return true;
    if (s.match.groups?.includes(group)) return true;
    return false;
  });
  if (candidates.length) return candidates[0]!;
  const fallback = SEED_FALLBACKS[group] ?? SEED_FALLBACKS.other!;
  return { ...fallback, kinds: [normalizedKind], services: [service], notes: `${fallback.notes}; kind=${kind}` };
}

function normalizeSeedKind(kind: string): string {
  const map: Record<string, string> = {
    chatbot_flow: "landing",
    web_page: "landing",
    store_page: "landing",
    workflow_diagram: "automation_recipe",
    analytics_report: "report_section",
    social_post: "ad_creative",
    video_script: "ad_creative",
    creative_graphic: "ad_creative",
    brand_asset: "content_page",
  };
  return map[kind] ?? kind;
}

